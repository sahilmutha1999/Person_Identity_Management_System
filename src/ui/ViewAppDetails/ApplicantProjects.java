    public void loadData() {
        for (Project p: applicant.getProjectList()) {
            txtName.setText(p.getName());
            cbType.setSelectedItem(p.getType());
            txtDescription.setText(p.getDescription());
            txtStartMM.setText(Integer.toString(p.getStartMonth()));
            txtStartYYYY.setText(Integer.toString(p.getStartYear()));
            txtEndMM.setText(Integer.toString(p.getEndMonth()));
            txtEndYYYY.setText(Integer.toString(p.getEndYear()));
        }
    }

