    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest w : userAccount.getWorkQueue().getWorkRequestList()){
            
            if (w.getSubject() instanceof JobRole) {
                JobRole j = (JobRole)w.getSubject();

                Object[] row = new Object[7];
                row[0] = j.getEmployer().getName();
                row[1] = j;
                row[2] = j.getLocation();
                row[3] = j.getPay();
                row[4] = w.getRequestDate();
                row[5] = w.getStatus();
                row[6] = w;
                model.addRow(row);
            }
        }
    }
    
