    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblJobs.getSelectedRow();

        if (selectedRow >= 0) {
            
            WorkRequest request = (WorkRequest) tblJobs.getValueAt(selectedRow, 6);
            JobRole jobRole = (JobRole) tblJobs.getValueAt(selectedRow, 1);
            if (request.getStatus().equalsIgnoreCase("Accepted")) {
                
                Date date = new Date();
                request.setStatus("Offer Accepted");
                request.setResolveDate(date);
                
                request.getSender().createNewEmployment(jobRole.getEmployer(), 
                        jobRole.getName(), date.getMonth() + 1, 
                        date.getYear() + 1900, 0, 0, "", true);
                
                JOptionPane.showMessageDialog(this, "Congratulations for accepting the offer.");
                populateTable();
                return;
            } 
            else if (request.getStatus().equalsIgnoreCase("Offer Accepted")) {
                JOptionPane.showMessageDialog(this, "Offer already Accepted.");
                return;
            }
            else if (request.getStatus().equalsIgnoreCase("Rejected")) {
                JOptionPane.showMessageDialog(this, "Recevied Reject!");
                return;
            }
            else {
                populateTable();
                JOptionPane.showMessageDialog(this, "Decision not received.");
                return;
            }

        } else {
            JOptionPane.showMessageDialog(null, "Choose a Job to accept the offer.");
            return;
        }
    }//GEN-LAST:event_btnAcceptActionPerformed


