    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String name = txtCertificationName.getText();
        String country = (String)cbCountry.getSelectedItem();
        
        CertificationOrganization co = new CertificationOrganization(name, country);
        business.getOrganizationDirectory().getOrganizationList().add(co);
        
        Person person = new Person();
        person.setName(name);
        
        UserAccount userAccount = new UserAccount(person, business);
        try{
            userAccount.setUsername(txtUsername.getText());
            userAccount.setPassword(txtPassword.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error Message", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        userAccount.setRole(new CertificationAuthorityRole());
        userAccount.setPerson(person);
        
        co.getPersonDirectory().getPersonList().add(person);
        co.getUserAccountDirectory().getUserAccountList().add(userAccount);
        
        
        JOptionPane.showMessageDialog(this, "Profile created successfully.");
        
        CertificationLogin cl = new CertificationLogin(
                rightProcessingContainer, leftProcessingContainer, business);
        rightProcessingContainer.add("CertificationLogin",cl);
        CardLayout layout = (CardLayout) rightProcessingContainer.getLayout();
        layout.next(rightProcessingContainer);
    }//GEN-LAST:event_btnRegisterActionPerformed


