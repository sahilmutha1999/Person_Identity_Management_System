    private void cbStudentMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cbStudentMousePressed
        // TODO add your handling code here:
        int applicantId = Integer.parseInt(
                cbStudent.getSelectedItem().toString().split(" - ")[0]);
        
        for (Applicant a: applicantList) {
            if (a.getId() == applicantId) {
                applicant = a;
                break;
            }
        }
        
        lblName.setText(applicant.toString());
    }//GEN-LAST:event_cbStudentMousePressed
    
    

