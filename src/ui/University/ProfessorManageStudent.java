    public void populateCourseCombo() {
        cbCourse.removeAllItems();
        
        for (Major m: universityOrganization.getUniversity().getMajorList()) {
            if (m.getProfessor() == userAccount) {
                cbCourse.addItem(m);
            }
        }
    }
    
    public void populateStudentCombo() {
        cbStudent.removeAllItems();
        
        major = (Major) cbCourse.getSelectedItem();
        for (WorkRequest w: universityOrganization.getWorkQueue().getWorkRequestList()) {
            if ((Major)w.getSubject() == major && w.getStatus().equals("Offer Accepted")) {
                cbStudent.addItem(w.getSender().getId() + " - " + w.getSender().getPerson().getName());
                applicantList.add(w.getSender());
            }
        }
    }

