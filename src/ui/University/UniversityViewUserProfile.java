    private void populatetable() {
        DefaultTableModel model = (DefaultTableModel) tblProfile.getModel();
        
        model.setRowCount(0);
        
        for (UserAccount u : universityOrganization.getUserAccountDirectory()
                .getUserAccountList()){
            Object[] row = new Object[4];
            row[0] = u.getRole().type;
            row[1] = u.getAssociatedPerson().getName();
            row[2] = u.getUsername();
            row[3] = u.getPassword();
            model.addRow(row);
        }
    }
