    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblJob.getModel();
        
        model.setRowCount(0);
        
        for (JobRole j : employerOrganization.getEmployer().getJobRoleList()){
            Object[] row = new Object[3];
            row[0] = j.getName();
            row[1] = j.getDomain();
            row[2] = j.getPay();
            model.addRow(row);
        }
    }

